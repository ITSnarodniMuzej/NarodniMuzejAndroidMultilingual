package com.example.windows10.androidmuzej.credits;

public class CreditsItemName {

    private String name;
    private String role;

    public CreditsItemName(String name, String role) {
        this.name = name;
        this.role = role;
    }

    public String getName() {
        return name;
    }

    String getRole() {
        return role;
    }
}
